l = []
for x in range(30):
    for y in range(20):
        su = 0
        d = (4 * 30 ** 0 + x * 30 ** 1 + 3 * 30 ** 2 + x * 30 ** 3
             + 2 * 30 ** 4 + x * 30 ** 5 + 1 * 30 ** 6 + 1 * 30 ** 7 + x * 30 ** 8)
        c = (y * 20 ** 0 + 4 * 20 ** 1 + y * 20 ** 2 + 3 * 20 ** 3
             + y * 20 ** 4 + 2 * 20 ** 5 + y * 20 ** 6 + 1 * 20 ** 7 + 1 * 20 ** 8)
        s = d - c
        while s > 0:
            su += s % 9
            s = s // 9
        l.append(su)
print(max(l))
